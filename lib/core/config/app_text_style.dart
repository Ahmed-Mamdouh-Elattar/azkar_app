import 'package:flutter/material.dart';

class AppTextStyle {
  static TextStyle styleBold18() {
    return const TextStyle(fontWeight: FontWeight.bold, fontSize: 18);
  }

  static TextStyle styleBold22() {
    return const TextStyle(fontSize: 22, fontWeight: FontWeight.bold);
  }

  static TextStyle styleBold30() {
    return const TextStyle(fontSize: 30, fontWeight: FontWeight.bold);
  }

  static TextStyle styleMedium20() {
    return const TextStyle(fontSize: 20, fontWeight: FontWeight.w500);
  }

  static TextStyle styleMedium18() {
    return const TextStyle(fontSize: 18, fontWeight: FontWeight.w500);
  }

  static TextStyle styleMedium16() {
    return const TextStyle(fontSize: 16, fontWeight: FontWeight.w500);
  }

  static TextStyle styleMedium14() {
    return const TextStyle(fontSize: 14, fontWeight: FontWeight.w500);
  }

  static TextStyle styleRegular16() {
    return const TextStyle(fontWeight: FontWeight.w400, fontSize: 16);
  }

  static TextStyle styleRegular18() {
    return const TextStyle(fontWeight: FontWeight.w400, fontSize: 18);
  }
}
